	<h3>Könnt ihr Beschweren.Rocks auch für ein anderes Anliegen bereitsstellen?</h3>
	<p>
		Theoretisch ja. Praktisch haben wir vermutlich keine Zeit dazu, da wir Beschweren.Rocks in
		unserer Freizeit vorantreiben. Mit vielen Themen kennen wir uns auch nicht so gut aus und sind
		nicht tief genug drin, dass wir ähnlich solide Vorschläge wie für Beschweren.Rocks liefern
		könnten.
	</p>
	<p>
		Falls du trotzdem glaubst, dass du ein Anliegen hast, das von einer Seite basierend auf der
		Software in Beschweren.Rocks profitieren würde, hast du im Wesentlichen zwei Möglichkeiten:
	</p>
	<ul>
		<li>
			Eine Mail an <a href="mailto:hallo@beschweren.rocks">hallo@beschweren.rocks</a> schreiben und nachfragen,
			ob wir dazu was machen können und werden. Das ist in den meisten Fällen aber sehr unwahrscheinlich
		</li>
		<li>
			Unser Programmcode ist öffentlich auf <a
				href="https://github.com/chloe-the-catgirl/corona-noergel-app"
				rel="noopener">GitHub</a
			>. Die Lizenz ist frei und das Tool kann geforked und für den eigenen Bedarf angepasst werden.
			Das Frontend basiert auf Svelte mit TypeScript und TailwindCSS. Die Inhalte werden aus
			JSON-Dateien generiert.
		</li>
	</ul>
	<h3>Wieso richtet ihr euch an deutsche Politik, habt aber ein Impressum in Österreich?</h3>
	<p>
		Rechtlich müssen wir eines vorhalten. Wir wissen aber sehr genau, dass es Menschen gibt, die
		sehr gegenteilige Ansichten über die Notwendigkeiten von Maßnahmen zur Eindämmung dieser
		Pandemie haben, und dass sich unter diesen auch einige befinden, bei denen wir gerne vermeiden
		würden, dass sie sich bei uns zum Kaffee einladen. Der Impressumskontakt erfüllt die rechtlichen
		Vorgaben für ein Impressum - und schützt uns dabei weitestgehend.
	</p>
	<h2>Ich habe Post von euch bekommen / Ich bin Politiker*in</h2>
