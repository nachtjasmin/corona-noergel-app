<div class="prose mt-4 mb-8 dark:prose-invert">
	<h2 class="sr-only">Allgemeine Fragen</h2>
	<h3>Was ist das hier?</h3>
	<p>
		Hier gibt es Vorschläge für Formulierungen und Kontaktinformationen für pandemiepolitische
		Appelle. In wenigen Klicks gibt es einen Vorschlag für ein Schreiben und die Kontaktdaten, um
		das erzeugte Schreiben per Brief, Fax oder per Mail zu verschicken.
	</p>
	<h3>Was soll das?</h3>
	<p>
		Wir sind der Ansicht, dass es auf gesellschaftlicher und individueller Ebene wichtig und
		deutlich besser als der Status Quo wäre, wenn es deutlichere und stärkere Leitlinien gegen diese
		Pandemie aus der Politik gäbe.<br />
		Gleichzeitig ist es oft schwierig, eine Beschwerde, eine Bitte, eine Rückmeldung oder eine Forderung
		an <em>die Politik</em> zu formulieren. Hier liefern wir Vorschläge.
	</p>
	<h3>Wer ist wir? Wer steht hinter Beschweren.Rocks?</h3>
	<p>
		<a href="https://github.com/xenein" rel="noopener">Xenia</a> und
		<a href="https://github.com/chloe-the-catgirl" rel="noopener">Jasmin</a> entwickeln die Software,
		die Beschweren.Rocks betreibt, setzen Themen und stellen die Formulierungsvorschläge zusammen. Beide
		entwerfen und erstellen hauptberuflich Software.
	</p>
	<h3>Was mache ich, wenn ich einen Fehler auf Beschweren.Rocks gefunden habe?</h3>
	<p>
		Gerne unter <a href="mailto:hallo@beschweren.rocks">hallo@beschweren.rocks</a> Bescheid geben.
		Falls es sich um einen Programmierfehler handelt, gerne auch eine
		<a href="https://github.com/chloe-the-catgirl/corona-noergel-app" rel="noopener"
			>Issue auf GitHub</a
		> öffnen.
	</p>
