<div class="prose mt-4 mb-8 dark:prose-invert">
	<p>
		Am 22.01. haben die Gesundheitsministieren in einer Telefonkonferenz weitgehende <a
			href="https://www.gmkonline.de/Beschluesse.html?uid=268&jahr=2022"
			>Einschränkungen für PCR-Tests</a
		> beschlossen.
	</p>
	<p>
		<b>Update (25.01.2022):</b> Wir haben die Kontaktdaten um die jeweiligen Staats- und Senatskanzleien
		erweitert, sodass diese nun auch auf einfachem Wege kontaktiert werden können.
	</p>
	<p>
		<b>Update (01.02.2022):</b> Es gibt nun deutlich mehr Beschwerden und Forderungen. Die sind nun auch
		gruppiert, was im ersten Schritt dann ausgewählt werden kann. Dazu noch Fehlerbehebungen und Verbesserungen
		in der UX und Barrierfreiheit.
	</p>
	<p>
		<b>Update (08.02.2022):</b> Teilerfolg bei PCR-Tests.
		<a href="https://twitter.com/MDRAktuell/status/1491006160874483713">Laut MDR</a>
		kündigt Lauterbach an, dass Anspruch auf PCR-Tests bei positivem Schnelltest weiterhin bestehen soll.
	</p>
	<p>
		<b>Update (02.03.2022):</b> PCR-Tests rückläufig, Anpassung der Daten. Laut dem <i>ALM e.V.</i>,
		einem Zusammenschluss von Laboren,
		<a
			href="https://www.alm-ev.de/kw08-deutlich-weniger-sars-cov-2-pcr-tests-bei-weiter-hoher-positivrate-untererfassung-koennte-hoch-sein/"
			>könnte die Unterfassung der Corona-Infektionen hoch sein</a
		>. Daher wurden die Beschwerden aktualisiert, um auf diesen Umstand aufmerksam zu machen.
	</p>
	<p>
		<b>Update (14.03.2022):</b> Die angekündigte Aufhebung sämtlicher Schutzmaßnahmen bereitet uns starke
		Bauchschmerzen. Die Daten des Gesundheitsausschusses wurden ergänzt, um an dieser Stelle etwas Druck
		aufbauen zu können.
	</p>
	<p>
		<b>Update (16.07.2022):</b> Wie erwartet, sind die Zahlen im Sommer deutlich in die Höhe gegangen.
		Im Vergleich zu den beiden Sommern davor, ist die Inzidenz "jenseits von Gut und Böse", und zwar
		kontinuierlich im oberen dreistelligen Bereich. Maßnahmen gibt es keine, stattdessen wurden aus "Kostengründen"
		noch die Antigen-Tests kostenpflichtig gemacht.
	</p>
