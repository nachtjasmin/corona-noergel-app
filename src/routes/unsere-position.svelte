<h1 class="text-2xl font-bold">Unsere Position zur Pandemie und dessen Eindämmung</h1>

<div class="prose mt-4 mb-8 dark:prose-invert">
	<p>
		Da wir gefragt worden, warum wir diese Webseite denn überhaupt hier betreiben, einmal kurz und
		bündig unsere Position:
	</p>
	<h2>Tests</h2>
	<p>
		Tests sind gut und wichtig für die Datenlage. Und Wien (Österrich) hat es richtig vorgemacht.
	</p>
	<ul>
		<li>
			Eine <strong>Einführung von Gurgeltests</strong> ist sinnvoll. Anwendungsfehler sind bei dieser
			Methode deutlich unwahrscheinlicher als bei den hier üblichen Abstrichen.
		</li>
		<li>
			In Wien gab es <strong>zahlreiche Abgabestellen</strong>, etwa an Tankstellen, Drogerien, etc.
			Dies nimmt eine große Barriere, wenn es darum geht, sich schnell testen zu lassen.
		</li>
		<li>
			Die Ergebnisse der Tests sind digital per App angebunden gewesen. Auch hierzulande wäre eine
			solche Integration über die Corona-Warn-App möglich.
		</li>
		<li><strong>Es handelt sich um PCR-Tests.</strong></li>
		<li>
			Optimalerweise ist die Nutzung <strong>kostenlos</strong> oder zumindest sehr günstig, etwa über
			ein Flatrate-Modell für 9€ pro Monat. Angesichts der Inflation und der damit gestiegenen Belastung
			für alltägliche Ausgaben, sind Corona-Tests meist niedrig priorisiert und nur für Wohlhabende regelmäßig
			durchführbar.
		</li>
	</ul>
	<h2>Masken</h2>
	<p>
		Masken sind nützlich. Wir haben <a
			href="https://www.tagesschau.de/inland/grippe-impfung-121.html"
			>einen Stamm Grippe durch Corona-Maßnahmen weltweit fast ausgerottet</a
		>. Krank sein und krank werden ist schlecht, dessen sind wir uns wohl einig. Die Übertragung
		hoch ansteckender Viren, wie etwa SARS-COV-2 ist bestmöglich zu unterbinden, um die
		Langzeitfolgen für Individuen und Gesellschaft gleichermaßen abzufedern. Daher fordern wir
		<strong>in der Erkältungssaison ab jetzt immer Maskenpflicht</strong>. Und zwar konsequent,
		beim:
	</p>
	<ul>
		<li>Einkaufen</li>
		<li>Behördenbesuchen</li>
		<li>im Nah- und Fernverkehr</li>
		<li>in Innenräumen (ja auch in Schulen)</li>
		<li>größeren Ansammlungen (Konzerte, Fußballspiele, etc.)</li>
	</ul>
	<p>
		Dabei geht es nicht darum, konsequent FFP2-Maskenpflicht zu fordern, sondern es reicht bereits
		ein grundlegender Schutz aus. So sind selbst die vielen Stoffmasken, die anfangs der Pandemie
		verteilt wurden, besser als überhaupt kein Schutz. Sofern die Erkältungswellen sehr heftig
		werden, können ja immer noch Mindeststandards eingefordert und festgelegt werden.
	</p>
	<h2>Impfungen</h2>
	<p>
		Impfungen sind gut, Impfungen sind nützlich. Aber diese funktionieren immer noch am Besten, wenn
		<strong>möglichst viele Menschen versorgt werden</strong> können. Daher ist es uns wichtig, dass:
	</p>
	<ul>
		<li><strong>weltweit die Impfungen gegen SARS-COV-2 vorangebracht werden</strong></li>
		<li>
			<strong>Menschen hierzulande für Impftermine eingeladen werden</strong>, so wie es die Freie
			Hansestadt Bremen getan hat
		</li>
		<li>
			eine <strong>allgemeine Impfpflicht</strong> für das Corona-Virus durchgesetzt wird, um endlich
			mal eine bessere Immunität aufbauen zu können
		</li>
		<li>
			die Verfügbarkeit auch für reguläre Grippe-Impfungen ausgeweitet und kostenlos zugänglich
			gemacht wird.
		</li>
	</ul>
	<h2>Luftfilter</h2>
	<p>
		Luftfilter sind eine großartige Technik für Innenräume mit vielen Menschen. Der Bundestag setzt
		diese bereits konsequent ein, es ist daher angebracht, die Verfügbarkeit von Luftfiltern auf
		eine Vielzahl an Innenräumen auszuweiten. Dazu sollten <strong
			>Fördermittel in ausreichenden Mengen und zu zugänglichen Bedingungen</strong
		> bereitgestellt werden, damit unter Anderem an folgenden Orten Filter eingebaut werden können:
	</p>
	<ul>
		<li>Veranstaltungs/Kultur-Locations</li>
		<li>Gaststätten</li>
		<li>Schulen</li>
		<li>Universitäten und Fachhochschulen</li>
		<li>…</li>
	</ul>
	<h2>Digitalisierung</h2>
	<p>
		Trotz aller Vorsichtsmaßnahmen werden Menschen krank. Damit sich die durchgesetzten Maßnahmen im
		Rahmen der freiheitlich-demokratischen Grundordnung befinden, ist eine <strong
			>konsequente Datenerfassung notwendig</strong
		>. Um eine Evaluation schnell durchführen zu können, ist es wichtig, dass diese Daten
		<strong>digital</strong> vorliegen. Aber auch etwa für die Fernlehre ist eine Digitalisierung notwendig.
		Daher fordern wir:
	</p>
	<ul>
		<li>
			den Ausbau einer ausreichend schnellen Internetverbindung
			<ul>
				<li>für Schulen, Unis, Lernende und Lehrende für hybride Konzepte</li>
				<li>für eine zuverlässige Vernetzung von Behörden, Laboren, etc.</li>
			</ul>
		</li>
		<li>
			die Bereitstellung passender Software und den Aufbau digitaler Infrastrukturen, etwa für die
			Fernlehre.
		</li>
	</ul>
	<h2>Verlässliche und vorhersehbare Verordnungen</h2>
	<p>
		Wir als Bevölkerung sind auf eine gewisse <strong>Planbarkeit</strong> angewiesen. Erst hü und
		dann hott ist für Privatpersonen und Unternehmen gleichermaßen bescheiden und untermauert nur
		das Misstrauen in den Staat. Daher ist eine <strong>Einführung eines Stufenplans</strong> sinnvoll.
		Dieser sollte:
	</p>
	<ul>
		<li>den Schutz der Bevölkerung priorisieren</li>
		<li>sich nicht jede Woche ändern</li>
		<li>nachvollziehbar begründet sein</li>
		<li>
			durchaus mehrgleisig aufgebaut sein, etwa wenn die Krankenhäuser voll sind, aber die
			Infektionszahlen niedrig
		</li>
	</ul>
</div>
